var randomNumber = 10;

switch (randomNumber) {
    case 1:
        console.log("Es un uno");
        break;
    case 10:
        console.log("Es un diez");
        break;
    case 100:
        console.log("Es un cien");
        break;
    default:
        console.log("No es ninguna de la anteriores");
        break;
}