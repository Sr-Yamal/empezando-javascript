// Valores que son VERDADEROS
Boolean("a") // Cadenas
Boolean(4) // Números
Boolean([]) // Arrays
Boolean({}) // Objetos
Boolean(function(){}) // funciones

// Valores que son FALSOS
Boolean(); // Vacío
Boolean(0); // El número 0
Boolean(null); // Valor null
Boolean(NaN); // Not a Number
Boolean(undefined); // Valor noo definido
Boolean(false); // Valor falso
Boolean(""); // Cadenas vacías